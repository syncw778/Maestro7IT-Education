x=int(input("Введите первое число:"))
y=int(input("Введите второе число:"))

print(f"Результат сложения: {x}+{y} =", x + y)
print(f"Результат вычитания: {x}-{y} =", x - y)
print(f"Результат умножения: {x}*{y} =", x * y)
print(f"Результат деления: {x}/{y} =", x / y)
print(f"Результат целочисленного деления: {x}//{y} =", x // y)
print(f"Результат возведения в степень: {x}**{y} =", x ** y)
print(f"Результат остатка от деления: {x}%{y} =", x % y)
