'''
# Запрос данных у пользователя
x = int(input("Введите первое число (x): "))
y = int(input("Введите второе число (y): "))

# Вывод результатов арифметических операций
print(f"Результат сложения: {x} + {y} =", x + y)   # Сложение
print(f"Результат вычитания: {x} - {y} =", x - y)   # Вычитание
print(f"Результат умножения: {x} * {y} =", x * y)   # Умножение
print(f"Результат деления: {x} / {y} =", x / y)   # Деление (обычное)
print(f"Результат целочисленного деления: {x} // {y} =", x // y) # Целочисленное деление
print(f"Результат остатка от деления: {x} % {y} =", x % y)   # Остаток от деления
print(f"Результат возведения в степень: {x} ** {y} =", x ** y) # Возведение в степень
'''

def calculator():
    """
    Простой интерактивный калькулятор.

    Запрашивает у пользователя два числа и операцию, затем выполняет вычисление.
    Поддерживаемые операции:
    - '+'  → сложение
    - '-'  → вычитание
    - '*'  → умножение
    - '/'  → деление (обычное)
    - '//' → целочисленное деление
    - '%'  → остаток от деления
    - '**' → возведение в степень

    Пример использования:
    >>> Введите первое число: 10
    >>> Введите второе число: 3
    >>> Выберите операцию (+, -, *, /, //, %, **): *
    >>> Результат: 10 * 3 = 30
    """
    try:
        # Ввод чисел
        x = float(input("Введите первое число: "))
        y = float(input("Введите второе число: "))
        
        # Выбор операции
        operation = input("Выберите операцию (+, -, *, /, //, %, **): ").strip()
        
        # Вычисление результата
        if operation == '+':
            result = x + y
        elif operation == '-':
            result = x - y
        elif operation == '*':
            result = x * y
        elif operation == '/':
            if y == 0:
                raise ZeroDivisionError("Деление на ноль невозможно!")
            result = x / y
        elif operation == '//':
            if y == 0:
                raise ZeroDivisionError("Целочисленное деление на ноль невозможно!")
            result = x // y
        elif operation == '%':
            if y == 0:
                raise ZeroDivisionError("Остаток от деления на ноль невозможен!")
            result = x % y
        elif operation == '**':
            result = x ** y
        else:
            raise ValueError("Неизвестная операция!")
        
        # Вывод результата
        print(f"Результат: {x} {operation} {y} = {result}")
    
    except ValueError as e:
        print(f"Ошибка: {e} (проверьте ввод чисел)")
    except ZeroDivisionError as e:
        print(f"Ошибка: {e}")
    except Exception as e:
        print(f"Неизвестная ошибка: {e}")

# Запуск калькулятора
if __name__ == "__main__":
    calculator()